"""
Dataset Preparation
-------------------
Tools for building a YOLO-format flower detection dataset.

YOLO label format (one .txt file per image):
  <class_id> <cx_norm> <cy_norm> <w_norm> <h_norm>
  - All values normalized 0–1 relative to image size
  - cx, cy = bounding box center
  - w, h = bounding box width and height

Directory structure expected by YOLOv8:
  ml/data/flowers/
    dataset.yaml          ← dataset config
    images/
      train/              ← training images (.jpg/.png)
      val/                ← validation images
      test/               ← test images (optional)
    labels/
      train/              ← label .txt files (same filename as image)
      val/
      test/

Data collection tips for aerial flower images:
  1. Mount camera same way as on drone (downward facing)
  2. Hold camera at multiple altitudes: 1m, 2m, 4m, 8m
  3. Capture in different lighting (morning, noon, overcast)
  4. Include non-flower backgrounds (grass, soil, leaves)
  5. Capture partially occluded flowers for robustness
  6. Minimum ~200 labeled images per class for decent results
     ~2000 images for production-quality detection
"""

from __future__ import annotations

import os
import random
import shutil
from pathlib import Path
from typing import List, Optional, Tuple

import cv2
import numpy as np
import yaml
from loguru import logger


DATASET_YAML_TEMPLATE = """
# Flower Detection Dataset
# Generated by ml/dataset.py

path: {dataset_path}
train: images/train
val:   images/val
test:  images/test

nc: 3   # Number of classes
names:
  0: flower_open
  1: flower_closed
  2: flower_cluster

# Download source (optional):
# roboflow: https://universe.roboflow.com/search?q=flower+detection
"""


def create_dataset_structure(base_path: str = "ml/data/flowers") -> Path:
    """Create the YOLO dataset directory structure."""
    base = Path(base_path)
    for split in ["train", "val", "test"]:
        (base / "images" / split).mkdir(parents=True, exist_ok=True)
        (base / "labels" / split).mkdir(parents=True, exist_ok=True)

    yaml_path = base / "dataset.yaml"
    if not yaml_path.exists():
        yaml_path.write_text(DATASET_YAML_TEMPLATE.format(
            dataset_path=str(base.resolve())
        ))
        logger.info(f"Created dataset.yaml at {yaml_path}")

    logger.info(f"Dataset structure created at {base}")
    return base


def download_from_roboflow(
    workspace: str,
    project: str,
    version: int,
    api_key: str,
    output_path: str = "ml/data/flowers",
) -> str:
    """
    Download a labeled flower dataset from Roboflow.

    Requires: pip install roboflow
    Get API key from: https://app.roboflow.com/settings/api

    Recommended public datasets:
      workspace="public",  project="flower-recognition-lwkpw", version=1
      workspace="public",  project="flowers-xbhsp", version=2
    """
    from roboflow import Roboflow

    logger.info(f"Downloading {workspace}/{project} v{version} from Roboflow...")
    rf = Roboflow(api_key=api_key)
    project_obj = rf.workspace(workspace).project(project)
    dataset = project_obj.version(version).download(
        "yolov8",
        location=output_path,
        overwrite=False,
    )
    logger.info(f"Dataset downloaded to {dataset.location}")
    return dataset.location


def split_dataset(
    image_dir: str,
    label_dir: str,
    output_base: str = "ml/data/flowers",
    train_frac: float = 0.70,
    val_frac: float = 0.20,
    seed: int = 42,
):
    """
    Split a flat directory of images/labels into train/val/test splits.
    Useful when you have a single directory of raw labeled images.

    Args:
        image_dir: Directory with .jpg/.png images
        label_dir: Directory with matching YOLO .txt label files
        output_base: Base directory for the split dataset
        train_frac: Fraction for training (remaining goes to test)
        val_frac: Fraction for validation
        seed: Random seed for reproducibility
    """
    images = sorted(Path(image_dir).glob("*.jpg")) + sorted(Path(image_dir).glob("*.png"))
    logger.info(f"Found {len(images)} images to split")

    random.seed(seed)
    random.shuffle(images)

    n = len(images)
    n_train = int(n * train_frac)
    n_val = int(n * val_frac)

    splits = {
        "train": images[:n_train],
        "val":   images[n_train:n_train + n_val],
        "test":  images[n_train + n_val:],
    }

    base = create_dataset_structure(output_base)

    for split_name, split_images in splits.items():
        for img_path in split_images:
            # Copy image
            dst_img = base / "images" / split_name / img_path.name
            shutil.copy(img_path, dst_img)

            # Copy matching label
            label_path = Path(label_dir) / img_path.with_suffix(".txt").name
            if label_path.exists():
                dst_lbl = base / "labels" / split_name / label_path.name
                shutil.copy(label_path, dst_lbl)
            else:
                logger.warning(f"No label for {img_path.name}")

        logger.info(f"{split_name}: {len(split_images)} images")


def visualize_labels(
    dataset_path: str = "ml/data/flowers",
    split: str = "val",
    num_samples: int = 5,
    save_dir: Optional[str] = None,
):
    """
    Draw bounding boxes on sample images to verify label quality.
    Saves annotated images to save_dir (or shows them interactively).
    """
    class_names = ["flower_open", "flower_closed", "flower_cluster"]
    class_colors = [(0, 255, 100), (0, 100, 255), (255, 200, 0)]

    base = Path(dataset_path)
    images = list((base / "images" / split).glob("*.jpg"))[:num_samples]

    for img_path in images:
        img = cv2.imread(str(img_path))
        if img is None:
            continue
        h, w = img.shape[:2]

        label_path = base / "labels" / split / img_path.with_suffix(".txt").name
        if not label_path.exists():
            continue

        with open(label_path) as f:
            for line in f:
                parts = line.strip().split()
                if len(parts) != 5:
                    continue
                cls_id, cx, cy, bw, bh = int(parts[0]), *map(float, parts[1:])
                x1 = int((cx - bw / 2) * w)
                y1 = int((cy - bh / 2) * h)
                x2 = int((cx + bw / 2) * w)
                y2 = int((cy + bh / 2) * h)
                color = class_colors[cls_id % len(class_colors)]
                cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
                cv2.putText(img, class_names[cls_id], (x1, y1 - 5),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.5, color, 1)

        if save_dir:
            Path(save_dir).mkdir(parents=True, exist_ok=True)
            out_path = Path(save_dir) / img_path.name
            cv2.imwrite(str(out_path), img)
            logger.info(f"Saved annotated: {out_path}")
        else:
            cv2.imshow(img_path.name, img)
            cv2.waitKey(0)

    if not save_dir:
        cv2.destroyAllWindows()


def augment_with_drone_backgrounds(
    images_dir: str,
    labels_dir: str,
    backgrounds_dir: str,
    output_dir: str,
    num_augmented: int = 200,
):
    """
    Paste flower images onto realistic drone-view backgrounds
    (grass, soil, mulch) to increase dataset diversity.

    This is especially useful when you only have close-up flower photos
    but need aerial perspectives.

    Args:
        images_dir: Cropped flower images (transparent PNG or with solid background)
        labels_dir: Corresponding YOLO labels for the original images
        backgrounds_dir: Aerial/overhead background images (grass, garden views)
        output_dir: Output directory for augmented images + labels
        num_augmented: How many composite images to generate
    """
    backgrounds = list(Path(backgrounds_dir).glob("*.jpg")) + \
                  list(Path(backgrounds_dir).glob("*.png"))
    flower_images = list(Path(images_dir).glob("*.png"))  # PNG for transparency

    if not backgrounds or not flower_images:
        logger.error("Need both backgrounds and flower images")
        return

    Path(output_dir).mkdir(parents=True, exist_ok=True)
    out_imgs = Path(output_dir) / "images"
    out_lbls = Path(output_dir) / "labels"
    out_imgs.mkdir(exist_ok=True)
    out_lbls.mkdir(exist_ok=True)

    for i in range(num_augmented):
        bg_path = random.choice(backgrounds)
        bg = cv2.imread(str(bg_path))
        if bg is None:
            continue
        bg_h, bg_w = bg.shape[:2]
        bg = cv2.resize(bg, (640, 640))
        bg_h, bg_w = 640, 640

        labels = []
        num_flowers = random.randint(1, 5)

        for _ in range(num_flowers):
            flower_path = random.choice(flower_images)
            flower = cv2.imread(str(flower_path), cv2.IMREAD_UNCHANGED)
            if flower is None:
                continue

            # Random scale: simulate different altitudes
            scale = random.uniform(0.05, 0.20)  # 5–20% of frame width
            fw = int(bg_w * scale)
            fh = int(fw * (flower.shape[0] / flower.shape[1]))
            flower_resized = cv2.resize(flower, (fw, fh))

            # Random position
            x = random.randint(0, bg_w - fw)
            y = random.randint(0, bg_h - fh)

            # Paste (handle alpha channel if present)
            if flower_resized.shape[2] == 4:
                alpha = flower_resized[:, :, 3:] / 255.0
                bg[y:y+fh, x:x+fw] = (
                    bg[y:y+fh, x:x+fw] * (1 - alpha) +
                    flower_resized[:, :, :3] * alpha
                ).astype(np.uint8)
            else:
                bg[y:y+fh, x:x+fw] = flower_resized[:, :, :3]

            # YOLO label (class 0 = flower_open for augmented)
            cx = (x + fw / 2) / bg_w
            cy = (y + fh / 2) / bg_h
            bw = fw / bg_w
            bh = fh / bg_h
            labels.append(f"0 {cx:.6f} {cy:.6f} {bw:.6f} {bh:.6f}")

        out_img_path = out_imgs / f"aug_{i:05d}.jpg"
        out_lbl_path = out_lbls / f"aug_{i:05d}.txt"
        cv2.imwrite(str(out_img_path), bg)
        out_lbl_path.write_text("\n".join(labels))

    logger.info(f"Generated {num_augmented} augmented images in {output_dir}")
